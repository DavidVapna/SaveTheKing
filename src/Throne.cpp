void Throne::handleCollision(GameObject& currObject)
{
    if (&currObject == this)
        return;
    currObject.handleCollision(*this);
}
//-----------------------------------------------------------------------------
void Throne::handleCollision(King& theKing)
{
    theKing.handleCollision(*this);
}
